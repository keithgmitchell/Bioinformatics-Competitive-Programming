from Bio import SeqIO

records = list(SeqIO.parse("splc.txt", "fasta"))
main_seq = str(records[0].seq)
for item in records[1:]:
    main_seq = main_seq.replace(str(item.seq), '')

rna = main_seq
protein = []
position = 0
while position < len(rna):
    protein.append(table[rna[position:position+3]])
    position += 3
print (''.join(protein).strip('Stop'))
